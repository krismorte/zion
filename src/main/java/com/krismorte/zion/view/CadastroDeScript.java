    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNome.getText().equals("") || txtScript.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os valores sao obrigatorios");
        } else {
            String nomeArquivo = txtNome.getText();
            if (!nomeArquivo.endsWith(".sql")) {//adiciona extens√£o
                nomeArquivo += ".sql";
            }
            String grupo = "";
            if (!(boxGrupo.getSelectedItem().toString().equals(""))) {
                grupo = boxGrupo.getSelectedItem().toString().toUpperCase();
            }
            Script scr = new Script(nomeArquivo, boxTipo.getSelectedItem().toString(), grupo, txtScript.getText());
            if (scr.add()) {
                if (scr.criouTipo) {
                    preencheBoxGrupo();
                    //Main.logApp.addMsgLog(CadastroDeServidor.class.getCanonicalName(), "Novo Tipo de script " + scr.getTipo() + " adicionado com sucesso");
                    logger.info("Novo Tipo de script " + scr.getTipo() + " adicionado com sucesso");
                }
                JOptionPane.showMessageDialog(this, "Script cadastrado com Sucesso!");
                logger.info("Script " + txtNome.getText() + " adicionado com sucesso");
                LimpaCampos.limpaCampos(this);
            } else {
                JOptionPane.showMessageDialog(this, "Falha no cadastro do Script!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

